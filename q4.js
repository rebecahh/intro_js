function rainbow(num){
  if (num==0){
    console.log("no color");
  }
  if (num==1){
    console.log("red");
  }
  if (num==2){
    console.log("orange");
  }
  if (num==3){
    console.log("yellow");
  }
  if (num==4){
    console.log("green");
  }
  if (num==5){
    console.log("blue");
  }
  if (num==6){
    console.log("purple");
  }
}

rainbow(0);
rainbow(1);
rainbow(2);
rainbow(3);
rainbow(4);
rainbow(5);
rainbow(6);
